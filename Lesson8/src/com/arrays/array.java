package com.arrays;

public class array {
    public static void main(String[] args) {
        int[] values = new int[5];
        values[0] = 10;
        values[1] = 2;
        for (int i = 0; i < values.length; i++) {
            int a = values[i];
            System.out.println(a);
        }
    }
}
